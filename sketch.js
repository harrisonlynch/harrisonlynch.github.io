function setup() {
	createCanvas(windowWidth, windowHeight);
	colorMode(RGB);
	noStroke();
	background(0);
	

	for (var j = 0; j < 39; j++) {
		for (var i = 0; i < 23; i++) {
			unit(i * 12 + 20, j * 12 + 20, 6, h[j][i], a[j][i], l[j][i]);
		}
	}
	
}

function unit (c_x, c_y, size, h, a, l) {
	size = size / 2;
	fill_top = color("red");
	fill_left = color("blue");
	fill_right = color("yellow");
	
	if (h == 1) {
		fill(fill_top);
		ellipse(c_x, c_y - size / Math.cos(PI/6), size, size);
	}
	
	if (a == 1) {
		fill(fill_left);
		ellipse(c_x - size, c_y + Math.tan(PI/6) * size, size, size);
	}
	
	if (l == 1) {
		fill(fill_right);
		ellipse(c_x + size, c_y + Math.tan(PI/6) * size, size, size);
	}
}